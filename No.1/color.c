// 检查当前颜色是否和相邻省份冲突
bool is_valid(int province, int color, int color_used[]) {
    for (int i = 0; i < PROVINCE_NUM; i++) {
        if (adjacency[province][i] == 1 && color_used[i] == color) {
            return false;
        }
    }
    return true;
}

// 递归回溯染色函数
bool backtrack(int province, int color_used[], int max_color) {
    // 染完
    if (province == PROVINCE_NUM) {
        return true;
    }

    // 尝试每种颜色
    for (int color = 1; color <= max_color; color++) {
        if (is_valid(province, color, color_used)) {
            color_used[province] = color;
            if (backtrack(province + 1, color_used, max_color)) {
                return true;
            }
            color_used[province] = 0; // 回溯
        }
    }
    return false;
}

// 非递归栈
bool non_recursive_color(int color_used[], int max_color) {
    int stack[PROVINCE_NUM];
    int stack_top = -1;
    int color_try[PROVINCE_NUM] = {0}; 
    stack[++stack_top] = 0; 

    while (stack_top >= 0) {
        int current = stack[stack_top];
        if (current == PROVINCE_NUM) {
            return true; 
        }

        bool found = false;
        for (int c = color_try[current] + 1; c <= max_color; c++) {
            if (is_valid(current, c, color_used)) {
                color_used[current] = c;
                color_try[current] = c;
                stack[++stack_top] = current + 1;
                found = true;
                break;
            }
        }

        if (!found) {
            color_used[current] = 0;
            color_try[current] = 0;
            stack_top--;
        }
    }
    return false;
}

// 输出染色结果
void print_result(int color_used[]) {
    printf("===== 中国省级行政区染色方案 =====\n");
    for (int i = 0; i < PROVINCE_NUM; i++) {
        printf("%-4s: %s\n", province_name[i], color_name[color_used[i] - 1]);
    }
}

int main() {
    int color_used[PROVINCE_NUM] = {0};

    printf("--- 四色染色（递归版）方案 ---\n");
    if (backtrack(0, color_used, 4)) {
        print_result(color_used);
    } else {
        printf("无法用4种颜色染色！\n");
    }

    // 重置数组
    for (int i = 0; i < PROVINCE_NUM; i++) {
        color_used[i] = 0;
    }

    printf("\n--- 三色染色可行性验证 ---\n");
    if (backtrack(0, color_used, 3)) {
        printf("可以用3种颜色染色！\n");
        print_result(color_used);
    } else {
        printf("无法用3种颜色染色！\n");
    }

    // 重置数组
    for (int i = 0; i < PROVINCE_NUM; i++) {
        color_used[i] = 0;
}

    printf("\n--- 四色染色（非递归版）方案 ---\n");
    if (non_recursive_color(color_used, 4)) {
        print_result(color_used);
    } else {
        printf("无法用4种颜色染色！\n");
    }

    return 0;
}